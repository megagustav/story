My personal goal was to recycle the driver into a functional object at no cost, using only objects and materials found in the school: Plywood was retrieved from a previous project (<i>cf.</i> <a href="#voronoi">Voronoi Chair</a>), the volume knob was found in a drawer filled with spare parts and the electronics were taken from old desktop speakers. The only new parts were the 3D printed component brackets. <br/><br/>
			The wooden front face was an experiment in CNC milling and 3D routing, processes I had never done before. The face was also meant as a natural element that contrasts with the clean, industrial casing. It mirrors the ventilation holes of the body to act as a speaker grille on the machined plywood face. At the end of the project, I donated the speaker to the school for people to play music in the workshop.<br><br>
